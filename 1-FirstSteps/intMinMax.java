public class intMinMax{
   public static void main(String[] args){
      int min = Integer.MIN_VALUE;
      int max = Integer.MAX_VALUE;
      System.out.println("Min Integer Val is = "+min);
      System.out.println("Max Integer Val is = "+max);
      
      System.out.println("\nMin Integer - 1 Val is = "+(min-1));
      System.out.println("Max Integer + 1 Val is = "+(max+1));
      
      
   }
}
