
/************************************EXAMPLE OF BYTE DATA TYPE************************************/          

public class byteEx{
   public static void main(String[] args){
      byte min = Byte.MIN_VALUE;          //MIN AND AX VALUES OF BYTE ARE ASSIGNED TO THE VARIABLES
      byte max = Byte.MAX_VALUE;
      
      System.out.println("Min Byte = "+min);
      System.out.println("Max Byte = "+max);
   }
}
