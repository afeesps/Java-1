public class floatDouble{
	public static void main(String[] args){
		float fMin = Float.MIN_VALUE;
		float fMax = Float.MAX_VALUE;
		System.out.println("Float Min = " + fMin);
		System.out.println("Float Max = " + fMax);
		
		double dMax = Double.MAX_VALUE;
		double dMin = Double.MIN_VALUE;
		System.out.println("\nDouble Min = " + dMin);
		System.out.println("Double Max = " + dMax);
		
		int intA = 5 / 3;
		float floatA = 5F / 3F;
		double doubleA = 5D / 3D;
		System.out.println("Int = " + intA);
		System.out.println("Float = " + floatA);
		System.out.println("Double = " + doubleA);
	}
}
